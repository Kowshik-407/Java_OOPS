public class Numbers {
    public static void main(String[] args) {
        int a = 15;
        int b = 7;

        int sum = a + b;
        System.out.println(sum);

        int sub = a - b;
        System.out.println(sub);

        int mul = a * b;
        System.out.println(mul);

        int div = a / b;
        System.out.println(div);  // It is the output of only int

        int rem = a % b;
        System.out.println(rem); 
    }
}